/**
 * Spread Operator
 * - 배열이나 객체의 속성을 해체하여 그 값을 개별 변수에 담을 수 있게 하는 자바스크립트 표현식
 * - 기존 것을 건드리지 않고, 새로운 객체를 만들 때 사용
 * 
 * restParameter
 * - 정해지지 않은 수 인수를 배열로 나타낼 수 있게 해줌(ES6)
 * - 객체, 배열, 함수의 매개변수에서 사용 가능
 * 
 * * 정리
 * - 둘의 모양은 같지만 하는 역할은 다름, 문맥, 상황을 통해 파악할 수 있음
 * - 함수 매개변수에 점(...)을 사용하는 경우 => rest Parameter
 * - 함수 호출에 쓰이면 => spread Operator
 * - rest parameter는 여러개의 인자를 받고 그것들을 합쳐서 새로운 배열/객체를 만듦
 * - Spread Operator는 기존의 변수를 펼쳐서 줌
 */

const test = (...args)=>{
    console.log(args);
}
test(1, 2, 3, 4);

